        private void displayMyCart() {
        try {
            ResultSet rs = dbh.displayCart(m.order_id);
            while(tblOrders.getRowCount() > 0)
            ((DefaultTableModel) tblOrders.getModel()).removeRow(0);

            int columns = rs.getMetaData().getColumnCount();
            while(rs.next()) {
                Object[] row = new Object[columns];
                for(int i = 1; i <= columns; i++)
                row[i - 1] = rs.getObject(i);

                ((DefaultTableModel) tblOrders.getModel()).insertRow(rs.getRow() - 1, row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
            
    public void getTotal() {
        double subtotal = 0;
        for(int i = 0; i < tblOrders.getRowCount(); i++)
            subtotal += Double.parseDouble(tblOrders.getValueAt(i, 6).toString());
        
        this.grand_total = subtotal + 20;
        txtSubtotal.setText(Double.toString(subtotal));
        txtGrandTotal.setText(Double.toString(grand_total));
    }
    
