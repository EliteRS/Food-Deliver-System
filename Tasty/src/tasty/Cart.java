    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int order_details_id = Integer.parseInt(txtOrderDetailsID.getText());
        int res = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "Delete Record",
            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(res == JOptionPane.YES_OPTION) {
            boolean success = dbh.deleteFromCart(order_details_id);

            if(success) {
                JOptionPane.showMessageDialog(this, "Deleted Successfully");
                displayMyCart();
                getTotal();
                txtOrderDetailsID.setText("");
                txtOrderID.setText("");
                txtProductID.setText("");
                txtProductName.setText("");
                txtPrice.setText("0.0");
                txtQuantity.setText("");
                txtTotalPrice.setText("0.0");
                btnUpdate.setEnabled(false);
                btnDelete.setEnabled(false);
            }
            else
            JOptionPane.showMessageDialog(this, "Failed to Delete");

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

