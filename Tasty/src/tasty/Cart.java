    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int order_details_id = Integer.parseInt(txtOrderDetailsID.getText());
        int order_id = Integer.parseInt(txtOrderID.getText());
        int product_id = Integer.parseInt(txtProductID.getText());
        String product_name = txtProductName.getText();
        double price = Double.parseDouble(txtPrice.getText());
        int quantity = Integer.parseInt(txtQuantity.getText());
        double total_price = price * (double) quantity;

        boolean success = dbh.updateCart(order_details_id, order_id, product_id, product_name, price, quantity, total_price);

        if(success) {
            JOptionPane.showMessageDialog(this, "Updated Successfully");
            displayMyCart();
            getTotal();
        }
        else
        JOptionPane.showMessageDialog(this, "Failed to Update");

        getTotal();
    }//GEN-LAST:event_btnUpdateActionPerformed

