    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int prod_id = Integer.parseInt(txtID.getText());
        int res = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", 
                "Delete Record", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(res == JOptionPane.YES_OPTION) {
            boolean success = dbh.deleteOrderMenu(prod_id);

            if(success) {
                JOptionPane.showMessageDialog(this, "Deleted Successfully");
                displayMenu();
            }
            else
            JOptionPane.showMessageDialog(this, "Failed to Delete");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

