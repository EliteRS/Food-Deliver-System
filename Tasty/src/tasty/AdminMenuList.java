    private void displayMenu() {
        try {
            ResultSet rs = dbh.displayAllMenu();
            while(tblMenu.getRowCount() > 0)
            ((DefaultTableModel) tblMenu.getModel()).removeRow(0);

            int columns = rs.getMetaData().getColumnCount();
            while(rs.next()) {
                Object[] row = new Object[columns];
                for(int i = 1; i <= columns; i++)
                row[i - 1] = rs.getObject(i);

                ((DefaultTableModel) tblMenu.getModel()).insertRow(rs.getRow() - 1, row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminMenuList.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int product_id = Integer.parseInt(txtID.getText());
        String product_name = txtName.getText();
        String category = txtCategory.getText();
        double price = Double.parseDouble(txtPrice.getText());

        boolean success = dbh.addMenu(product_id,product_name, category, price);

        if(success) {
            JOptionPane.showMessageDialog(this, "Added Successfully");
            displayMenu();
        }
        else
        JOptionPane.showMessageDialog(this, "Failed to Add");
    }//GEN-LAST:event_btnAddActionPerformed

